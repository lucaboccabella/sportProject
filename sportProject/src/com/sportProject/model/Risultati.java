package com.sportProject.model;

public class Risultati {
	private String home;
	private String away;
	private int homeres;
	private int awayres;
	public Risultati() {}
	public String getHome() {
		return home;
	}
	public void setHome(String home) {
		this.home = home;
	}
	public String getAway() {
		return away;
	}
	public void setAway(String away) {
		this.away = away;
	}
	public int getHomeres() {
		return homeres;
	}
	public void setHomeres(int homeres) {
		this.homeres = homeres;
	}
	public int getAwayres() {
		return awayres;
	}
	public void setAwayres(int awayres) {
		this.awayres = awayres;
	}
	
}
